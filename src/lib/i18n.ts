type MessageKey = keyof typeof messages.ru;

export function setLocale(nextLocale: Locale) {
  locale.value = nextLocale;
  if (typeof localStorage !== 'undefined') localStorage.setItem(STORAGE_KEY, nextLocale);
}

export function t(key: MessageKey): string {
  return messages[locale.value][key] || messages.ru[key] || key;
}

export function useI18n() {
  return {
    locale,
    currentLocaleLabel: computed(() => locale.value.toUpperCase()),
    t,
    setLocale,
  };
}

watchEffect(() => {
  if (typeof document !== 'undefined') document.documentElement.lang = locale.value;
});
